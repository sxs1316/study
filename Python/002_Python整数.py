# 运算
a = 2
b = 10
c = 3

# + 加
print('a+b:', a + b)

# - 减
print('a-b:', a - b)

# * 乘
print('a*b:', a * b)

# / 除
print('b/a:', a / b)

# % 取模
print('b%a:', b % a)
print('b%c:', b % c)

# ** 幂运算
print('a**b:', a ** b)
